You can determine if a number is even or odd by examining its last digit. 
If the last digit is 0, 2, 4, 6, or 8, the number is even. If the last digit is 1, 3, 5, 7, or 9, the number is odd.
So, you don't need to use any conditions or loops to make this determination; 
you just need to look at the last digit of the number.