public class CharAtIndex {
    public static void main(String[] args) {
        String originalString = "Tops Technologies!";
        
        char charAtIndex0 = originalString.charAt(0);
        System.out.println("The character at position 0 is " + charAtIndex0);
        

        char charAtIndex10 = originalString.charAt(10);
        System.out.println("The character at position 10 is " + charAtIndex10);
    }
}
